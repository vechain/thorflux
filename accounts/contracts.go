var tokenTransferEvent = common.HexToHash("0xddf252ad1be2c89b69c2b068fc378daa952ba7f163c4a11628f55a4df523b3ef")

type ContractInfo struct {
	Name  string
	Count int
	// 'account', 'contract', 'erc721', 'erc20'
	Type string
}

type Stats struct {
	KnownAccountTxs     map[string]ContractInfo
	ClauseCount         int
	TxCount             int
	KnownNftCount       int
	KnownErc20Count     int
	TotalTokenTransfers int
}

func updateContractInfo(knownContractTXs map[string]ContractInfo, clause transaction.Clause, name string, contractType string) {
	existing, ok := knownContractTXs[clause.To().Hex()]
	if !ok {
		knownContractTXs[clause.To().Hex()] = ContractInfo{
			Name:  name,
			Count: 1,
			Type:  contractType,
		}
	} else {
		existing.Count++
		knownContractTXs[clause.To().Hex()] = existing
	}
}

func GetStats(block *client.ExpandedBlock) *Stats {
	knownContractTXs := make(map[string]ContractInfo)
	knownErc20Count := 0
	knownNftCount := 0
	txCount := 0
	clauseCount := 0
	tokenContractTransfers := 0

	for _, tx := range block.Transactions {
		knownTx := false
		for _, clause := range tx.Clauses {

			if clause.To() == nil {
				continue
			}

			if knownContracts[*clause.To()] != "" {
				knownTx = true
				clauseCount++
				updateContractInfo(knownContractTXs, clause, knownContracts[*clause.To()], "contract")
			}

			if nftContracts[*clause.To()] != "" {
				clauseCount++
				knownNftCount++
				knownTx = true
				updateContractInfo(knownContractTXs, clause, nftContracts[*clause.To()], "erc721")
			}

			if fungibleTokens[*clause.To()] != "" {
				clauseCount++
				knownErc20Count++
				knownTx = true
				updateContractInfo(knownContractTXs, clause, fungibleTokens[*clause.To()], "erc20")
			}

			if knownAddresses[*clause.To()] != "" {
				clauseCount++
				knownTx = true
				updateContractInfo(knownContractTXs, clause, knownAddresses[*clause.To()], "account")
			}
		}
		if knownTx {
			txCount++
		}

		for _, output := range tx.Outputs {
			for _, event := range output.Events {
				if len(event.Topics) > 0 && event.Topics[0] == tokenTransferEvent {
					tokenContractTransfers++
				}
			}
		}
	}

	stats := &Stats{
		KnownAccountTxs:     knownContractTXs,
		KnownNftCount:       knownNftCount,
		KnownErc20Count:     knownErc20Count,
		TxCount:             txCount,
		ClauseCount:         clauseCount,
		TotalTokenTransfers: tokenContractTransfers,
	}

	return stats
}

